package com.mj;

public class LogBean {


    /**
     * 日志ID
     */
    private String logId;
    /**
     * 日志内容
     */
    private String logText;

    public String getLogId() {
        return logId;
    }

    public String getLogText() {
        return logText;
    }

    public void setLogId(String logId) {
        this.logId = logId;
    }

    public void setLogText(String logText) {
        this.logText = logText;
    }

}